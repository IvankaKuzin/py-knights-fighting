def battle(knights_config: dict) -> dict:
    lancelot = knights_config.get("lancelot")
    lancelot_prepare = Knight(lancelot)
    lancelot_prepare.all_preparations()

    arthur = knights_config.get("arthur")
    arthur_prepare = Knight(arthur)
    arthur_prepare.all_preparations()

    mordred = knights_config.get("mordred")
    mordred_prepare = Knight(mordred)
    mordred_prepare.all_preparations()

    red_knight = knights_config.get("red_knight")
    red_knight_prepare = Knight(red_knight)
    red_knight_prepare.all_preparations()

    battle1 = Battle(lancelot_prepare, mordred_prepare)
    battle2 = Battle(arthur_prepare, red_knight_prepare)

    battle1.battle_round()
    battle2.battle_round()

    return Battle.battle_result
